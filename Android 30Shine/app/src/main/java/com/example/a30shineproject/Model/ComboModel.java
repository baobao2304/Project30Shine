package com.example.a30shineproject.Model;

public class ComboModel {
    int checkCombo;
    int imgCombo;

    public ComboModel(int checkCombo, int imgCombo) {
        this.checkCombo = checkCombo;
        this.imgCombo = imgCombo;
    }

    public int getCheckCombo() {
        return checkCombo;
    }

    public void setCheckCombo(int checkCombo) {
        this.checkCombo = checkCombo;
    }

    public int getImgCombo() {
        return imgCombo;
    }

    public void setImgCombo(int imgCombo) {
        this.imgCombo = imgCombo;
    }
}
